listaValor = [5, 4, 10, 80, 60]

#Somar todos os itens da lista
somaLista = sum(listaValor)
print(somaLista)

#Encontrar o maior valor
maiorValor = max(listaValor)
print(maiorValor)

#Encontrar o menor valor
menorValor = min(listaValor)
print(menorValor)

#Ordenar a lista
listaValor.sort()
print(listaValor)