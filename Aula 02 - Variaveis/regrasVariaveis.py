#Regras Variaveis

#Regra 1 - As variáveis não podem começar com número, caracteres especiais, virgulas, pontos ou hífen.

#Regra 2 - Não pode contem espaços na declaração das variaveis
#camelCase
nomeCompleto = "Pedro Henrique"
print(nomeCompleto)
#snake_case
nome_completo = "Henrique Nathan"
print(nome_completo)

#Regra 3 - As variaveis não podem utilizar palavras-chave na sua declaração

# ['False', 'None', 'True', 'and', 'as', 'assert', 'break', 'class', 'continue', 'def', 'del', 'elif', 'else', 'except', 'finally', 'for', 'from', 'global', 'if', 'import', 'in', 'is', 'lambda', 'nonlocal', 'not', 'or', 'pass', 'raise', 'return', 'try', 'while', 'with', 'yield']

print = "Pedro"
print(print)