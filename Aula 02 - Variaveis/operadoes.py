#Operadores aritméticos

#Operador Soma
n1 = 5 
n2 = 10
soma = n1 + n2 
print("A soma foi igual a " , soma)

#Operador de Subtração
n1 = 5
n2 = 3
subtracao = n1 - n2
print("A subtração é igual a ", subtracao)

#Operador de multiplicação
n1 = 7
n2 = 4
multiplicacao = n1 * n2
print("A multiplicação é igual a " , multiplicacao)

#Operador de Divisão
n1 = 10
n2 = 2
divisao = n1 / n2
print("A divisão é igual a " ,  divisao)

#Operado de Potência
n1 = 10
n2 = 2
potencia = n1 ** n2
print("A potência é igual a " ,  potencia)

#Operador Módulo
n1 = 10
n2 = 5
modulo = n1 % n2
print("O resto da divisão é igual a " , modulo)

#Operador de divisão inteira
n1 = 10
n2 = 3
divisaoInteira = n1 // n2
print("A divisão inteira é igual a " , divisaoInteira)

