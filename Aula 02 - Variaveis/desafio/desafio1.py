# Crie um algoritmo que receba um número e mostre o seu dobro, triplo e seu quadrado

numero = int(input("Digite um número: "))

dobro = numero * 2
triplo = numero * 3
quadrado = numero ** 2

print(f"O dobro de {numero} é igual a {dobro} ")
print(f"O triplo de {numero} é igual a {triplo}")
print(f"O quadrado de {numero} é igual a {quadrado}")