#Operador de Adição (+)
n1 = 5 
n2 = 10
soma = n1 + n2
print(soma)

#Operador de Subtração (-)
n1 = 10
n2 = 2
subtracao = n1 - n2
print(subtracao)

#Operador Divisão (/)
n1 = 50
n2 = 5
divisao = n1 / n2
print(divisao)

#Operador Multiplicação (*)
n1 = 10
n2 = 5
multiplicacao = n1 * n2
print(multiplicacao)

#Operador Módulo (%)
n1 = 6
n2 = 3
modulo = n1 % n2
print(modulo)

#Operador Divisão Inteira (//)
n1 = 5
n2 = 2
divisao_inteira = 5 // 2
print(divisao_inteira)

#Operador de Potenciação (**)
base = 2
expoente = 3
potencia = base ** expoente
print(potencia)

#Lista Prioridade
# 1 - **
# 2 - * , / , % , //
# 3 - + , -